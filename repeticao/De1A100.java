package aula30Entrega.repeticao;

public class De1A100 {
    public static void main(String[] args) {
       int contador = 1;

        for (int i = 1; i <= 100; i++){
            System.out.println(i);
        }

        while (contador <= 100){
            System.out.println(contador);
            contador++;
        }


    }

}


